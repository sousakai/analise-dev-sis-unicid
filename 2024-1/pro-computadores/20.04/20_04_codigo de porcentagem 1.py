salario = float(input("Digite seu salário bruto: "))
imposto = float (input("Digite a porcententagem de desconto: "))
desconto = salario * imposto/100
salarioliquido = salario - desconto
print("Seu salário líquido é igual a:", salarioliquido, ".")

